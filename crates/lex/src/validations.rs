/// Function copied directly from `std::mem::validations`:
/// https://github.com/rust-lang/rust/blob/5d26145dee8c58cf3b973a8337d6b771f5f66cd8/library/core/src/str/validations.rs#L232-L257
///
/// The function is marked as unstable in `std::mem::validations` since it is
/// supposed to be used internally, so this copy allows us to use it in stable.
///
/// Given a first byte, determines how many bytes are in this UTF-8 character.
#[inline]
pub fn utf8_char_width(b: u8) -> usize {
    UTF8_CHAR_WIDTH[b as usize] as usize
}
